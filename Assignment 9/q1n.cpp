#include <iostream>
#include <queue>
#include <vector>
using namespace std;
void BFS(int start, vector<vector<int>>& adj, int V) {
 vector<bool> visited(V, false);
 queue<int> q;
 visited[start] = true;
 q.push(start);
 cout << "BFS Traversal: ";
 while (!q.empty()) {
 int node = q.front();
 q.pop();
 cout << node << " ";
 for (int neighbor : adj[node]) {
 if (!visited[neighbor]) {
 visited[neighbor] = true;
 q.push(neighbor);
 }
 }
 }
}
int main() {
 int V = 5;
 vector<vector<int>> adj(V);
 adj[0] = {1, 2};
 adj[1] = {0, 3, 4};
 adj[2] = {0, 4};
 adj[3] = {1};
 adj[4] = {1, 2};
 BFS(0, adj, V);
 return 0;
}